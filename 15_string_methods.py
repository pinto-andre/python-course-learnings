"""
(Here are just some examples, visit the documentation above for the entire list)

1. **capitalize() -** Converts the first character of a string to uppercase and the rest to lowercase.

2. **casefold() -** Similar to lower(), but more aggressive in converting characters to lowercase, useful for case-insensitive string comparisons.

3. **lower() -** Converts all characters in a string to lowercase.

4. **upper() -** Converts all characters in a string to uppercase.

5. **title() -** Converts the first character of each word in a string to uppercase.

6. **swapcase() -** Swaps the case of each character in a string (converts uppercase to lowercase and vice versa).

7. **strip([chars]) -** Removes leading and trailing characters (whitespace by default) from a string.

8. **lstrip([chars]) -** Removes leading characters (whitespace by default) from a string.

9. **rstrip([chars]) -** Removes trailing characters (whitespace by default) from a string.

10. **count(substring[, start[, end]]) -** Counts the occurrences of a substring in a given string. You can also specify optional start and end positions.

11. **find(substring[, start[, end]]) -** Returns the lowest index of a substring in a given string. Returns -1 if the substring is not found.

12. **index(substring[, start[, end]]) -** Similar to find(), but raises a ValueError if the substring is not found.

13. **replace(old, new[, count]) -** Replaces occurrences of a substring with another substring. Optionally, you can specify the maximum number of replacements with the 'count' parameter.

14. **startswith(prefix[, start[, end]]) -** Checks if a string starts with a specified prefix. Returns True or False.

15. **endswith(suffix[, start[, end]]) -** Checks if a string ends with a specified suffix. Returns True or False.

16. **isalpha() -** Returns True if all characters in a string are alphabetic (consists only of letters), otherwise False.

17. **isdigit() -** Returns True if all characters in a string are digits, otherwise False.

18. **isspace() -** Returns True if all characters in a string are whitespace, otherwise False.

19. **islower() -** Returns True if all characters in a string are lowercase, otherwise False.

20. **isupper() -** Returns True if all characters in a string are uppercase, otherwise False.

21. **join(iterable) -** Concatenates each element of an iterable (e.g., a list) into a single string, using the original string as a separator.

22. **split([sep[, maxsplit]]) -** Splits a string into a list of substrings based on the specified separator (default is whitespace). You can also specify the maximum number of splits with 'maxsplit'.

23. **partition(sep) -** Splits a string into three parts based on the first occurrence of a specified separator. Returns a tuple containing the part before the separator, the separator itself, and the part after the separator.

24. **rpartition(sep) -** Similar to partition(), but splits from the rightmost occurrence of the separator.

25. *format(*args, **kwargs) -** Formats a string by replacing placeholders with values. This method allows for complex string formatting.
"""